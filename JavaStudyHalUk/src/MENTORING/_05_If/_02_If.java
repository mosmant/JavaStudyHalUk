package MENTORING._05_If;

public class _02_If {

    public static void main(String[] args) {

        int a = 3;
        int b = 5;

        if (a == b) {
            System.out.println("They are equal.");
        }

        if (a != b) {
            System.out.println("They are not equal");
        }

        System.out.println("-------------------------");

        String s1 = "We";
        String s2 = "You";



        if (s1.equals(s2)) {
            System.out.println("They are equal");
        }

        if (!s1.equals(s2)) {
            System.out.println("They are not equal");
        }

    }
}





