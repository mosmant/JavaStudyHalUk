package Replit._05_Loops;

public class _15_for_loop_8 {

    public static void main(String[] args) {

        /*
        do while loop kullanarak a'dan z'ye bütün harfleri yazdırınız.
         */

        //Kodu aşağıya yazınız.

        char sayac = 'a';
        char bitis = 'z';

        do {
            System.out.println(sayac);
            sayac++;


        } while (sayac <= bitis);

    }
}
