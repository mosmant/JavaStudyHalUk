package _JavaStudy._01_DataType_ScannerClass;

public class Soru_01 {
	/*
	  \n (or \r): starts from a new line
	          \t:  horizontal tab
	          \\: prints a back slash
	          \': prints single quote
	          \": prints double quote

	 */

	
	public static void main(String[] args) {
		//       Soru :   Ekrana      "Hello ", "World  \ /"        şeklinde yazınız

		System.out.println("   \"Hello \" , \"World \\ / \"    ");
		// Pazartesi kelimesini her harfini ayrı bir satıra gelecek şekile yazdırınız.

		System.out.println("P\na\nz\na\nr\nt\ne\ns\ni");

		System.out.println("Hello Cybertek\n Batch 12 students");
		
		System.out.print("Tomorrow is off \n");
		System.out.print("True\r");
		System.out.println("False\n\n\n");
		
		System.out.println("\tClass starts tomorrow");
		
		System.out.println("\\muhtar");
		System.out.println("\'Java ");
		
		System.out.println("Book is called \"Game of Throne\" ");
		
		
		
	}
	

}
