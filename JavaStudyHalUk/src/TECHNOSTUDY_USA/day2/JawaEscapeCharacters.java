package TECHNOSTUDY_USA.day2;

public class JawaEscapeCharacters {

    public static void main(String[] args ){
        // new line
        System.out.println( "Hello \nhi!");
        // tab
        System.out.println( "\tHello \tHello");
        // quote using escape character
        // (to print regular quote)
        System.out.println(" \" ");
        // to print single quote
        System.out.println(" \' ");
        // backslash\\
        System.out.println(" \\ ");
    }
}
